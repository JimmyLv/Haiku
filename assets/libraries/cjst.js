	/* Accepts a string or DOM element.
	 * Recursively iterates through every chinese letter and wraps it with a custom element, with wich
	 * we can attach events, style, and do special stuff.
	 * Returns: nothing.
	 */
	var recurseDOMNode = function(input, worker, callback) { // okay for up to a thousand or so chars.....
		// some minimal validation
		if (!isObjectAnElement(input)) {
			console.log('xx', input);
			throw 'Only DOM elements should be passed into cjst.recurseDOMNode()';
		}

		// a function to recurse through a dom and check for Chinese text
		var check = function(e, innerCallback) {
			if (e.childNodes.length === 0 && e.nodeType == 3) { // this is purely a end leaf node
				var text = e.textContent,
					lastTextGroup = '',
					group = createElement('cjst-group', '', '');
				if (hasChineseCharacters(text)) {
					for (var j = 0; j < text.length; j++) {
						if (isChineseCharacter(text[j])) {
							if (lastTextGroup.length > 0) { // non chinese text was built between the last chinese and here. group it into one node and append to dom group.
								group.appendChild(createElement('cjst-text', '', lastTextGroup));
								lastTextGroup = '';
							}
							var element = createElement('cjst-zi', '', text[j]);
							element.className = 'tile';
							element.addEventListener('mouseover', worker(element, text[j]), false);

							group.appendChild(element);
						} else {
							lastTextGroup += text[j];
						}
					}
					if (lastTextGroup.length > 0) // non chinese text was built between the last chinese and here. group it into one node and append to dom group.
						group.appendChild(createElement('cjst-text', '', lastTextGroup));
					e.parentNode.replaceChild(group, e);
				}
				if (innerCallback) innerCallback();
			} else {
				if (['SCRIPT','STYLE','#COMMENT'].indexOf(e.nodeName.toUpperCase()) == -1) {
					var doneCounter = 0;
					var allDone = function() {
						if (++doneCounter == e.childNodes.length)
							if (innerCallback)
								innerCallback();
					};
					for (var j = 0; j < e.childNodes.length; j++) {
						setTimeout((function(elementReference, adcb) {
							return function() {
								check(elementReference, adcb);
							};
						})(e.childNodes[j], allDone), 0);
					}
				} else {
					if (innerCallback) innerCallback();
				}
			}
		}

		// start processing at passed in node
		var allDoneCounter = 0;
		var allDoneCallback = function() {
			if (++allDoneCounter == input.childNodes.length)
				if (callback)
					callback();
		};
		for (var i = 0; i < input.childNodes.length; i++) {
			check(input.childNodes[i], allDoneCallback);
		}
	}


