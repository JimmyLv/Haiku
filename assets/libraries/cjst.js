	/* Accepts a string of chinese characters and a callback.
	 *
	 * Sends an HTTP request to a web service providing the translation of the characters and some additional information.
	 * 	* Note:
	 * 		- This is not a facility to translate sentences, merely words, phrases and idioms.
	 * Returns: An object containing translation and information about the Chinese characters.
	 * 	{
	 * 		traditional: '認識',
	 * 		translations: [
	 * 			'to know',
	 * 			'to recognize',
	 * 			'to be familiar with',
	 * 			'to get acquainted with sb',
	 * 			'knowledge',
	 * 			'understanding',
	 * 			'awareness',
	 * 			'cognition'
	 * 		]
	 * 	}
	 */
	var translatePhrase = function(input, options, callback) {
		var query = simplifiedToTraditional(input),
			md5 = md5ChineseText(query);
		httpRequest(__props.translationLocation + md5[0] + '/' + md5[1] + '/' + md5, 'GET', function(response) {
			if (callback) {
				if (response.st == 'ok') {
					try {
						callback(JSON.parse(response.data));
					} catch (err) {
						console.log('Error parsing JSON translation', err);
						console.log('Response', response.data);
						callback({ query: query, translation: [] });
					}
				} else {
					callback({ query: query, translation: [] });
				}
			}
		});
	}


