	/* Replace all instances of simplified Chinese characters with traditional Chinese characters.
	 */
	var simplifiedToTraditional = function(input) {
		// some minimal validation
		if (typeof input !== 'string')
			throw 'Only strings should be passed into cjst.simplifiedToTraditional()';

		// simply loop through the string and replace simplified chars with respective traditional char.
		var output = '',
			index = -1;
		for (var i = 0; i < input.length; i++) {
			index = __chineseConversion.simplified.indexOf(input[i]);
			if (index == -1) {
				output += input[i];
			} else {
				output += __chineseConversion.traditional[index];
			}
		}
		return output;
	}


