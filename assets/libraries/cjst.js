	/* Accepts a single (chinese) character.
	 * Returns: a boolean value indicating the likeliness that this library  can give a conversion for it.
	 */
	var isChineseCharacter = function(input) {
		// some minimal validation
		if (typeof input !== 'string' || input.length !== 1)
			throw 'Only single-character strings should be passed into cjst.isChineseCharacter()';

		// check that we have the required constants to work with further below
		if (typeof __props.smallestChineseUnicode != 'number' || typeof __props.biggestChineseUnicode != 'number') {
			var min = __chineseToPinyinLookup['zhōng'].charCodeAt(0), max = min;
			for (var k in __chineseToPinyinLookup) {
				if (__chineseToPinyinLookup.hasOwnProperty(k)) {
					var match = __chineseToPinyinLookup[k];
					for (var i = 0, code; i < match.length; code = match.charCodeAt(i++)) {
						if (code < min) min = code;
						if (code > max) max = code;
					}
				}
			}
			__props.smallestChineseUnicode = min;
			__props.biggestChineseUnicode = max;
		}

		// now process input
		var iv = input.charCodeAt(0);
		return iv >= __props.smallestChineseUnicode && iv <= __props.biggestChineseUnicode;
	}


	/* Accepts a string or DOM element.
	 * Returns: a boolean value indicating the likeliness that this library  can give a conversion for it.
	 */
	var hasChineseCharacters = function(input) {
		// some minimal validation
		if (typeof input !== 'string' && typeof input !== 'object')
			throw 'Only character strings and DOM elements should be passed into cjst.hasChineseCharacters()';

		// now process input
		if (typeof input === 'string') {
			for (var i = 0; i < input.length; i++)
				if (isChineseCharacter(input[i]))
					return true;
		} else {
			if (isObjectAnElement(input)) {
				return hasChineseCharacters(input.textContent);
			}
		}
		return false;
	}


